// 1. Criação das três variáveis de notas (usando let ou const)
let nota1 = 8.5;
let nota2 = 7.0;
let nota3 = 9.0;

// 2. Cálculo da média aritmética e armazenamento na variável 'media'

let media = (nota1 + nota2 + nota3) / 3;

// Exibindo o resultado no console
console.log("A média é: " + media.toFixed(2)); // toFixed(2) limita a 2 casas decimais
